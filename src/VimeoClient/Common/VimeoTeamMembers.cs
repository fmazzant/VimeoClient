namespace VimeoClient.Common
{
    using RestClient;

    /// <summary>
    /// Team Members
    /// </summary>
    public class VimeoTeamMembers
    {
        /// <summary>
        /// Vimeo Properties
        /// </summary>
        public VimeoProperties Properties { get; private set; }

        /// <summary>
        /// Vimeo
        /// </summary>
        public Vimeo Vimeo { get; private set; }

        /// <summary>
        /// Root Authorization
        /// </summary>
        public RestBuilder RootAuthorization() => Vimeo.RootAuthorization();

        /// <summary>
        /// Create a new instance of VimeoCategories class
        /// </summary>
        /// <param name="properties"></param>
        public VimeoTeamMembers(VimeoProperties properties)
           : this(new Vimeo(properties))
        {
        }

        /// <summary>
        /// Create a new instance
        /// </summary>
        /// <param name="vimeo"></param>
        public VimeoTeamMembers(Vimeo vimeo)
        {
            Vimeo = vimeo;
            Properties = vimeo.Properties;
        }

        #region [ Essentials ]
        /// <summary>
        /// This method returns information about the membership of the specified team. Usage is currently limited to the team join forms.
        /// </summary>
        /// <param name="code">The code corresponding to the desired team. This value appears under TeamUser > code.</param>
        /// <returns></returns>
        public virtual RestResult GetMembershipInformationAboutATeam(string code) => RootAuthorization()
            .Command($"/teammembers/{code}")
            .Get();
        #endregion
    }
}